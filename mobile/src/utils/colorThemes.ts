export interface ThemeColors {
  // Brand Colors
  primary: string;
  primaryLight: string;
  primaryDark: string;
  onPrimary: string; // Text color on primary buttons (theme-aware)

  // Background Colors
  background: string; // Main background
  cardBackground: string; // Card/container backgrounds
  modalBackground: string; // Modal backgrounds

  // Surface Colors (for badges, subtle containers)
  surfaceSubtle: string; // Subtle surface for badges, helper text boxes
  surfaceMuted: string; // More muted surface
  borderSubtle: string; // Subtle border for badges

  // Text Colors
  textPrimary: string; // Primary text
  textSecondary: string; // Secondary/helper text
  textTertiary: string; // Disabled/placeholder text

  // UI Element Colors
  border: string; // Border color
  divider: string; // Divider lines
  success: string; // Success states
  warning: string; // Warning states
  error: string; // Error states
  info: string; // Info states

  // Semantic Background Colors
  successBackground: string; // Light success background (e.g., #E8F5E9)
  warningBackground: string; // Light warning background (e.g., #FEF3C7)
  errorBackground: string; // Light error background (e.g., #FEE2E2)
  infoBackground: string; // Light info background

  // Text on Semantic Backgrounds
  onSuccess: string; // Text on success backgrounds
  onWarning: string; // Text on warning backgrounds
  onError: string; // Text on error backgrounds
  onInfo: string; // Text on info backgrounds

  // Premium / Gold Colors
  premium: string; // Premium gold color (#FFD700)
  premiumLight: string; // Light premium background (#FFF9E6)
  premiumDark: string; // Dark premium for text (#78350F or #B8860B)
  onPremium: string; // Text on premium buttons (white or dark)

  // Input field styling
  inputBorder: string;
  inputBackground: string;
  inputPlaceholder: string;

  // Toggle switch colors
  toggleTrackOn: string;
  toggleTrackOff: string;
  toggleThumb: string;

  // Disabled states
  buttonDisabled: string;
  buttonDisabledText: string;

  // Tab bar
  tabBarActive: string;
  tabBarInactive: string;

  // Interactive States
  buttonText: string; // Text on primary buttons (DEPRECATED - use onPrimary)
  linkText: string; // Link text

  // Shadow
  shadow: string; // Shadow color

  // Theme Metadata
  name: string;
  description: string;
}

