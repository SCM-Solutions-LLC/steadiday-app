export function getThemeColors(
  theme: ColorTheme,
  appearanceMode: AppearanceMode = "light",
  accessibilityMode: AccessibilityMode = "default"
): ThemeColors {
  // Determine if dark mode is active
  let isDark = false;
  if (appearanceMode === "dark") {
    isDark = true;
  } else if (appearanceMode === "system") {
    const systemColorScheme = Appearance.getColorScheme();
    isDark = systemColorScheme === "dark";
  }

  // Accessibility modes respect the user's light/dark preference
  if (accessibilityMode === "high-contrast") {
    return isDark ? DARK_HIGH_CONTRAST_THEMES[theme] : HIGH_CONTRAST_THEMES[theme];
  }

  if (accessibilityMode === "color-blind-friendly") {
    return isDark ? DARK_COLORBLIND_THEMES[theme] : COLORBLIND_THEMES[theme];
  }

  if (isDark) {
    return DARK_COLOR_THEMES[theme];
  }

  return COLOR_THEMES[theme];
}
